package com.example.in_shelterkidsdirectory;

public class Code {

    private String num;
    private String val;

    public Code(String num, String val) {
        this.num = num;
        this.val = val;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getVal() {
        return val;
    }

    public void setVal(String val) {
        this.val = val;
    }
}
